#Radius to volume conversion
#Get the input of radius 
r = int(input("Radius of the sphere : "))
#To calculate the surface area
surface_area = 4*22/7*r**2
#To calculate volume
volume = (4/3)*22/7*r**3
#Print the volume and surface area
print(f"The surface area of the sphere is {surface_area} and volume is {volume}")